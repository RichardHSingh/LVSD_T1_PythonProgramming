#============== CALCULATING AVERAGE IN ARRAY =====================

# array named numbers with numbers as value
numbers = [1, 2, 3, 4, 5]

# sum and length need to call the name of array(numbers) not "list"
average = sum(numbers) / len(numbers)

# prints the average of average variable
print("Average:", average)
# or
# f = formated string and is best to use 
print(f"Average in array is: {average}")